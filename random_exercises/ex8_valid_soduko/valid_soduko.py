def is_valid_soduko(board: list[list[str]]) -> bool:
    row = 0
    column = 0
    n = len(board)
    m = len(board[0])
    while row < n and column < m:
        for i in range(0, m):
            if board[row][i].isdigit():
                for j in range(i+1, m):
                    if board[row][j].isdigit() and board[row][i] == board[row][j]:
                        return False

        for i in range(0, n):
            if board[i][column].isdigit():
                for j in range(i+1, n):
                    if board[j][column].isdigit() and board[i][column] == board[j][column]:
                        return False

        row += 1
        column += 1

    return True







